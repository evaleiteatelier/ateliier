-- 5b. GESTOR DE CONTAS ÚNICO (acesso exclusivo à página Contas)
-- Só um utilizador de cada vez pode ter este papel. Para passar
-- o papel a outro utilizador é preciso confirmar a senha de quem
-- é gestor actualmente.
-- ==========================================

ALTER TABLE public.contas_adm ADD COLUMN IF NOT EXISTS gestor_contas BOOLEAN NOT NULL DEFAULT false;

CREATE UNIQUE INDEX IF NOT EXISTS idx_unico_gestor_contas
    ON public.contas_adm (gestor_contas)
    WHERE gestor_contas = true;

-- Usado pelo gate da página Contas: confirma se o email já com sessão iniciada é o gestor actual
CREATE OR REPLACE FUNCTION public.sou_gestor_contas(p_email TEXT)
RETURNS BOOLEAN
LANGUAGE sql
SECURITY DEFINER
AS $$
    SELECT EXISTS (
        SELECT 1 FROM public.contas_adm
        WHERE email = p_email AND gestor_contas = true
    );
$$;

-- Transfere o papel de gestor de contas para outro utilizador.
-- Exige a senha de quem detém o papel actualmente (excepto se ainda não houver nenhum gestor definido).
CREATE OR REPLACE FUNCTION public.definir_gestor_contas(p_novo_id UUID, p_senha_atual_gestor TEXT)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    v_atual public.contas_adm;
BEGIN
    SELECT * INTO v_atual FROM public.contas_adm WHERE gestor_contas = true LIMIT 1;

    IF v_atual.id IS NOT NULL THEN
        IF v_atual.id = p_novo_id THEN
            RETURN jsonb_build_object('sucesso', false, 'mensagem', 'Este utilizador já é o gestor de contas.');
        END IF;

        IF v_atual.senha <> crypt(COALESCE(p_senha_atual_gestor, ''), v_atual.senha) THEN
            RETURN jsonb_build_object('sucesso', false, 'mensagem', 'Senha do gestor de contas actual incorrecta.');
        END IF;

        UPDATE public.contas_adm SET gestor_contas = false WHERE id = v_atual.id;
    END IF;

    UPDATE public.contas_adm SET gestor_contas = true WHERE id = p_novo_id;

    RETURN jsonb_build_object('sucesso', true, 'mensagem', 'Gestor de contas actualizado com sucesso.');
END;
$$;

-- ==========================================
-- 6. REGISTAR CLIENTES PARA FINANÇAS AUTOMATICAMENTE
-- ==========================================

CREATE OR REPLACE FUNCTION public.registar_cliente_financas(p_nome TEXT, p_email TEXT, p_nif TEXT)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    v_id UUID;
    v_nif_real TEXT := NULLIF(TRIM(p_nif), '');
    v_email_real TEXT := NULLIF(TRIM(p_email), '');
BEGIN
    -- Se o NIF foi fornecido, procurar por ele primeiro
    IF v_nif_real IS NOT NULL THEN
        SELECT id INTO v_id FROM public.clientes_financas WHERE nif = v_nif_real LIMIT 1;
        
        IF v_id IS NOT NULL THEN
            -- Atualiza nome e email do cliente existente
            UPDATE public.clientes_financas 
            SET nome = p_nome, email = COALESCE(v_email_real, email)
            WHERE id = v_id;
            RETURN jsonb_build_object('sucesso', true, 'id', v_id);
        END IF;
    END IF;

    -- Se não achou por NIF, procurar por email
    IF v_email_real IS NOT NULL THEN
        SELECT id INTO v_id FROM public.clientes_financas WHERE email = v_email_real LIMIT 1;
        
        IF v_id IS NOT NULL THEN
            -- Atualiza o NIF (se não tinha) e o nome
            UPDATE public.clientes_financas 
            SET nome = p_nome, nif = COALESCE(v_nif_real, nif)
            WHERE id = v_id;
            RETURN jsonb_build_object('sucesso', true, 'id', v_id);
        END IF;
    END IF;

    -- Se não encontrou de forma alguma, cria novo
    INSERT INTO public.clientes_financas (nome, email, nif)
    VALUES (p_nome, v_email_real, v_nif_real)
    RETURNING id INTO v_id;

    RETURN jsonb_build_object('sucesso', true, 'id', v_id);
EXCEPTION WHEN OTHERS THEN
    RETURN jsonb_build_object('sucesso', false, 'erro', SQLERRM);
END;
$$;

-- ==========================================
-- 7. MIGRAÇÃO DE CLIENTES ANTIGOS PARA FINANÇAS
-- ==========================================
-- Este bloco insere automaticamente qualquer cliente que esteja nos pedidos mas não em clientes_financas
INSERT INTO public.clientes_financas (nome, email)
SELECT DISTINCT TRIM(p.nome), TRIM(p.email_cliente)
FROM public.pedidos p
WHERE p.nome IS NOT NULL AND p.nome != ''
  AND NOT EXISTS (
      SELECT 1 FROM public.clientes_financas c 
      WHERE LOWER(c.nome) = LOWER(TRIM(p.nome)) 
         OR (p.email_cliente IS NOT NULL AND LOWER(c.email) = LOWER(TRIM(p.email_cliente)))
  );

