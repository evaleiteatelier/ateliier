-- 4. FUNÇÃO DE LOGIN SEGURO PARA A TABELA PERSONALIZADA
-- ==========================================
CREATE OR REPLACE FUNCTION public.login_adm(p_email TEXT, p_senha TEXT)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    v_user public.contas_adm;
BEGIN
    -- Procurar o utilizador pelo email e verificar se a senha encriptada coincide
    SELECT * INTO v_user
    FROM public.contas_adm
    WHERE email = p_email AND senha = crypt(p_senha, senha);

    IF v_user.id IS NOT NULL THEN
        RETURN jsonb_build_object('sucesso', true, 'nome', v_user.nome, 'tipo', v_user.tipo_utilizador, 'email', v_user.email);
    ELSE
        RETURN jsonb_build_object('sucesso', false);
    END IF;
END;
$$;

