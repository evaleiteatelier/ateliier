-- A0) Listar Utilizadores (SECURITY DEFINER para contornar RLS)
CREATE OR REPLACE FUNCTION public.listar_adm()
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    RETURN (
        SELECT COALESCE(jsonb_agg(
            jsonb_build_object(
                'id', id,
                'nome', nome,
                'email', email,
                'tipo_utilizador', tipo_utilizador,
                'gestor_contas', gestor_contas,
                'criado_em', criado_em
            ) ORDER BY criado_em
        ), '[]'::jsonb)
        FROM public.contas_adm
    );
END;
$$;

-- A1) Alterar Senha com verificação da senha atual
CREATE OR REPLACE FUNCTION public.alterar_senha_adm(p_id UUID, p_senha_atual TEXT, p_senha_nova TEXT)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    v_user public.contas_adm;
BEGIN
    SELECT * INTO v_user FROM public.contas_adm
    WHERE id = p_id AND senha = crypt(p_senha_atual, senha);

    IF v_user.id IS NULL THEN
        RETURN jsonb_build_object('sucesso', false, 'mensagem', 'Senha atual incorreta');
    END IF;

    UPDATE public.contas_adm
    SET senha = crypt(p_senha_nova, gen_salt('bf'))
    WHERE id = p_id;

    RETURN jsonb_build_object('sucesso', true, 'mensagem', 'Senha alterada com sucesso');
END;
$$;

-- A) Criar Utilizador
CREATE OR REPLACE FUNCTION public.criar_adm(p_nome TEXT, p_email TEXT, p_senha TEXT, p_tipo TEXT)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    INSERT INTO public.contas_adm (nome, email, senha, tipo_utilizador)
    VALUES (p_nome, p_email, crypt(p_senha, gen_salt('bf')), p_tipo);
    
    RETURN jsonb_build_object('sucesso', true, 'mensagem', 'Utilizador criado com sucesso');
EXCEPTION WHEN OTHERS THEN
    RETURN jsonb_build_object('sucesso', false, 'mensagem', SQLERRM);
END;
$$;

-- B) Atualizar Utilizador
CREATE OR REPLACE FUNCTION public.atualizar_adm(p_id UUID, p_nome TEXT, p_email TEXT, p_senha TEXT, p_tipo TEXT)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    IF p_senha IS NULL OR p_senha = '' THEN
        -- Atualiza sem mexer na senha
        UPDATE public.contas_adm
        SET nome = p_nome, email = p_email, tipo_utilizador = p_tipo
        WHERE id = p_id;
    ELSE
        -- Atualiza tudo, incluindo senha encriptada
        UPDATE public.contas_adm
        SET nome = p_nome, email = p_email, tipo_utilizador = p_tipo, senha = crypt(p_senha, gen_salt('bf'))
        WHERE id = p_id;
    END IF;

    RETURN jsonb_build_object('sucesso', true, 'mensagem', 'Utilizador atualizado com sucesso');
EXCEPTION WHEN OTHERS THEN
    RETURN jsonb_build_object('sucesso', false, 'mensagem', SQLERRM);
END;
$$;

